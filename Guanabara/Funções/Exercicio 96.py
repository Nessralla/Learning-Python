# Faça um programa que recebe a largura e altura de um terreno retangular, utilizando uma função

def calcular_area():
    l = float(input("Largura do terreno: "))
    c = float(input("Comprimento do terreno: "))
    return print(f"A area de um terreno de {l} por {c} é de {l*c:.2f} metros quadrados")

calcular_area()

